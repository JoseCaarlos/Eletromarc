    private void txtCadFunCepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCadFunCepActionPerformed

        CWebServiceCep cWeb = new CWebServiceCep();
        //A ferramenta de busca ignora qualquer caracter que n?o seja n?mero.
        //caso a busca ocorra bem, imprime os resultados.
        cWeb.webServiceCep = cWeb.webServiceCep.searchCep(txtCadFunCep.getText());
        if (cWeb.webServiceCep.wasSuccessful()) {
            txtCadFunRua.setText(cWeb.webServiceCep.getLogradouroFull());
            txtCadFunCid.setText(cWeb.webServiceCep.getCidade());
            txtCadFunBai.setText(cWeb.webServiceCep.getBairro());
            cbCadFunUf.setSelectedItem(cWeb.webServiceCep.getUf());
            //caso haja problemas imprime as exce??es.
        } else {
            JOptionPane.showMessageDialog(null, "Erro numero: " + cWeb.webServiceCep.getResulCode());

            JOptionPane.showMessageDialog(null, "Descrição do erro: " + cWeb.webServiceCep.getResultText());
        }
    }//GEN-LAST:event_txtCadFunCepActionPerformed

